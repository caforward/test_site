        function closeCookie(){
            var cookieExpire = new Date();
            cookieExpire.setFullYear(cookieExpire.getFullYear() +2);
            document.cookie="OKCookie=1; expires=" + cookieExpire.toGMTString() + ";";
            docRoot.removeChild(okC);
        }
    })();

